//! Snowball stemmers for multiple languages.
//!
//! Generated by the Snowball compiler (https://snowballstem.org/) with the Zig backend.
//! Validation data: https://github.com/snowballstem/snowball-data
//! All stemmers pass at 0% failure rate against the official Snowball test vocabularies.

const std = @import("std");
const Allocator = std.mem.Allocator;
const analysis = @import("analysis.zig");
const stopwords = @import("stopwords.zig");
const Language = stopwords.Language;

const snowball = @import("snowball");

pub fn stem(alloc: Allocator, word: []const u8, lang: Language) ![]const u8 {
    if (lang == .english) return analysis.porter2Stem(alloc, word);

    const stem_fn: *const fn (*snowball.Env) bool = switch (lang) {
        .english => unreachable,
        .german => snowball.german.stem,
        .french => snowball.french.stem,
        .spanish => snowball.spanish.stem,
        .italian => snowball.italian.stem,
        .portuguese => snowball.portuguese.stem,
        .dutch => snowball.dutch.stem,
        .swedish => snowball.swedish.stem,
        .norwegian => snowball.norwegian.stem,
        .danish => snowball.danish.stem,
        .finnish => snowball.finnish.stem,
    };

    var env = snowball.Env.init(alloc);
    defer env.deinit();

    try env.setCurrent(word);
    _ = stem_fn(&env);
    const result = env.getCurrent();

    if (std.mem.eql(u8, result, word)) return word;
    return try alloc.dupe(u8, result);
}
