pub fn quantizeQ8_KBlock(input: []const f32, output: []u8) void {
    std.debug.assert(input.len == tensor_types.valuesPerBlock(.{ .known = .Q8_K }).?);
    std.debug.assert(output.len == tensor_types.bytesPerBlock(.{ .known = .Q8_K }).?);

    var max_vec: F32xN = @splat(0.0);
    comptime var max_i: usize = 0;
    inline while (max_i < 256) : (max_i += vec_len) {
        const vals: F32xN = input[max_i..][0..vec_len].*;
        max_vec = @max(max_vec, @abs(vals));
    }
    const max_abs = @reduce(.Max, max_vec);

    const scale = if (max_abs == 0.0) @as(f32, 1.0) else max_abs / 127.0;
    writeF32Le(output[0..4], scale);

    if (max_abs == 0.0) {
        @memset(output[4..260], 0);
        @memset(output[260..292], 0);
        return;
    }

    const inv_scale = if (max_abs == 0.0) 0.0 else 1.0 / scale;
    const inv: F32xN = @splat(inv_scale);
    const lo: F32xN = @splat(-127.0);
    const hi: F32xN = @splat(127.0);
    comptime var group: usize = 0;
    inline while (group < 16) : (group += 1) {
        var sum: i32 = 0;
        inline for (0..2) |chunk| {
            const i = group * 16 + chunk * vec_len;
            const vals: F32xN = input[i..][0..vec_len].*;
            const rounded = @round(vals * inv);
            const clipped = @min(@max(rounded, lo), hi);
            const q: I8xN = @intFromFloat(clipped);
            const raw: U8xN = @bitCast(q);
            output[4 + i ..][0..vec_len].* = raw;
            const q_i32: I32xN = @intCast(q);
            sum += @reduce(.Add, q_i32);
        }
        std.mem.writeInt(i16, output[260 + group * 2 ..][0..2], @intCast(sum), .little);
    }
}

