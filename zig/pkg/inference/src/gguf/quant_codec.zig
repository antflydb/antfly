pub fn quantizeQ8_1Block(input: []const f32, output: []u8) void {
    std.debug.assert(input.len == tensor_types.valuesPerBlock(.{ .known = .Q8_1 }).?);
    std.debug.assert(output.len == tensor_types.bytesPerBlock(.{ .known = .Q8_1 }).?);

    @memset(output, 0);
    const max_abs = maxAbs(input);
    const scale = if (max_abs == 0.0) @as(f32, 1.0) else max_abs / 127.0;
    writeF16Le(output[0..2], scale);

    const inv_scale = if (max_abs == 0.0) 0.0 else 1.0 / scale;
    var q_sum: i32 = 0;
    for (input, 0..) |value, i| {
        const q = if (max_abs == 0.0)
            @as(i8, 0)
        else
            @as(i8, @intFromFloat(@min(@max(std.math.round(value * inv_scale), -127.0), 127.0)));
        q_sum += q;
        output[4 + i] = @bitCast(q);
    }
    writeF16Le(output[2..4], scale * @as(f32, @floatFromInt(q_sum)));
}

