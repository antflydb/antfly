pub fn quantizeQ2_KBlock(input: []const f32, output: []u8) void {
    std.debug.assert(input.len == tensor_types.valuesPerBlock(.{ .known = .Q2_K }).?);
    std.debug.assert(output.len == tensor_types.bytesPerBlock(.{ .known = .Q2_K }).?);

    @memset(output, 0);
    var sub_scales: [16]f32 = undefined;
    var sub_mins: [16]f32 = undefined;
    var max_scale: f32 = 0.0;
    var max_min: f32 = 0.0;

    for (0..16) |sub| {
        const start = sub * 16;
        const sub_input = input[start .. start + 16];
        var min_v = sub_input[0];
        var max_v = sub_input[0];
        for (sub_input[1..]) |value| {
            min_v = @min(min_v, value);
            max_v = @max(max_v, value);
        }
        const min_mag = @max(-min_v, 0.0);
        const scale = if (max_v + min_mag == 0.0) @as(f32, 1.0) else (max_v + min_mag) / 3.0;
        sub_scales[sub] = scale;
        sub_mins[sub] = min_mag;
        max_scale = @max(max_scale, scale);
        max_min = @max(max_min, min_mag);
    }

    const d = if (max_scale == 0.0) @as(f32, 1.0) else max_scale / 15.0;
    const dmin = if (max_min == 0.0) @as(f32, 1.0) else max_min / 15.0;
    for (0..16) |sub| {
        const sc: u8 = if (max_scale == 0.0) 0 else @intFromFloat(@min(@max(std.math.round(sub_scales[sub] / d), 0.0), 15.0));
        const min_q: u8 = if (max_min == 0.0) 0 else @intFromFloat(@min(@max(std.math.round(sub_mins[sub] / dmin), 0.0), 15.0));
        output[sub] = sc | (min_q << 4);
    }
    writeF16Le(output[16..18], d);
    writeF16Le(output[18..20], dmin);

    for (0..16) |sub| {
        const scale = if (sub_scales[sub] == 0.0) @as(f32, 1.0) else sub_scales[sub];
        const inv_scale = if (sub_scales[sub] == 0.0) 0.0 else 1.0 / scale;
        const chunk = sub / 8;
        const group = (sub % 8) / 2;
        const l_base = (sub % 2) * 16;
        const q_base = 20 + chunk * 32;
        const shift: u3 = @intCast(group * 2);
        for (0..16) |i| {
            const q = quantizeUnsigned(input[sub * 16 + i] + sub_mins[sub], inv_scale, 3);
            output[q_base + l_base + i] |= q << shift;
        }
    }
}

