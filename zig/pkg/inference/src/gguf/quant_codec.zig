        }
    }
}

fn makeRampF32(comptime len: usize, comptime start: f32, comptime step: f32) [len]f32 {
    var out: [len]f32 = undefined;
    for (0..len) |i| out[i] = start + step * @as(f32, @floatFromInt(i));
    return out;
}
