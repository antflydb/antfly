// SIMD width / vector aliases.  The matmul-shaped decoder kernels below
// (`dotXBlock` and `accumulateXBlock`) are AVX2-friendly at 8 lanes; we
// don't autodetect AVX-512 because a stable comptime constant lets the
// kernel bodies stay simple.  Native-compute imports these via
// `quant_codec.vec_len` etc. so there's a single canonical SIMD width
// across the encoder + decoder + dispatch surface.
pub const vec_len: usize = 8;
pub const F32xN = @Vector(vec_len, f32);
pub const I32xN = @Vector(vec_len, i32);
pub const I8xN = @Vector(vec_len, i8);
pub const U8xN = @Vector(vec_len, u8);

/// Decode a little-endian fp16 pair into f32.  The block scales `d`
/// and `dmin` for every quant format are stored as fp16 in the
/// per-block header.  Public so the matmul kernels in native_compute
/// can reuse it without duplicating the logic.
pub fn decodeFp16Le(lo: u8, hi: u8) f32 {
    const bits: u16 = @as(u16, lo) | (@as(u16, hi) << 8);
    const half: f16 = @bitCast(bits);
    return @floatCast(half);
}

/// Decode a little-endian f32 from 4 bytes.  Used for prepared-Q4K
/// block headers (where d/dmin live as f32 instead of fp16).
pub fn readF32Le(src: []const u8) f32 {
    std.debug.assert(src.len == 4);
    return @bitCast(std.mem.readInt(u32, src[0..4], .little));
}

