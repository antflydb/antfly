pub fn quantizeQ3_KBlock(input: []const f32, output: []u8) void {
    std.debug.assert(input.len == tensor_types.valuesPerBlock(.{ .known = .Q3_K }).?);
    std.debug.assert(output.len == tensor_types.bytesPerBlock(.{ .known = .Q3_K }).?);

    @memset(output, 0);
    var raw_scales: [16]u8 = undefined;
    var sub_scales: [16]f32 = undefined;
    var max_scale: f32 = 0.0;
    for (0..16) |sub| {
        const sub_input = input[sub * 16 ..][0..16];
        const sub_max = maxAbs(sub_input);
        const scale = if (sub_max == 0.0) @as(f32, 1.0) else sub_max / 4.0;
        sub_scales[sub] = scale;
        max_scale = @max(max_scale, scale);
    }

    const d = if (max_scale == 0.0) @as(f32, 1.0) else max_scale / 31.0;
    for (0..16) |sub| {
        raw_scales[sub] = if (max_scale == 0.0) 32 else @intFromFloat(@min(@max(std.math.round(sub_scales[sub] / d) + 32.0, 0.0), 63.0));
    }
    packQ3KScaleData(output[96..108], raw_scales);
    writeF16Le(output[108..110], d);

    for (0..16) |sub| {
        const scale = if (sub_scales[sub] == 0.0) @as(f32, 1.0) else sub_scales[sub];
        const inv_scale = if (sub_scales[sub] == 0.0) 0.0 else 1.0 / scale;
        const chunk = sub / 8;
        const group = (sub % 8) / 2;
        const l_base = (sub % 2) * 16;
        const q_base = 32 + chunk * 32;
        const shift: u3 = @intCast(group * 2);
        const hm_bit: u3 = @intCast(chunk * 4 + group);

        for (0..16) |i| {
            const q_signed = if (inv_scale == 0.0)
                @as(i32, 0)
            else
                @as(i32, @intFromFloat(@min(@max(std.math.round(input[sub * 16 + i] * inv_scale), -4.0), 3.0)));
            const encoded: u8 = @intCast(q_signed + 4);
            output[q_base + l_base + i] |= (encoded & 0x03) << shift;
            output[l_base + i] |= ((encoded >> 2) & 0x01) << hm_bit;
        }
    }
}

