fn dequantizeIQ1M(data: []const u8, output: []f32) !void {
    const block_size = tensor_types.bytesPerBlock(.{ .known = .IQ1_M }).?;
    const values_per_block = tensor_types.valuesPerBlock(.{ .known = .IQ1_M }).?;
    if (data.len % block_size != 0) return error.InvalidQuantizedDataSize;
    if (output.len > (data.len / block_size) * values_per_block) return error.InvalidQuantizedOutputSize;

    var out_off: usize = 0;
    var block_index: usize = 0;
    while (block_index * block_size < data.len and out_off < output.len) : (block_index += 1) {
        const block = data[block_index * block_size .. (block_index + 1) * block_size];
        const qs = block[0..32];
        const qh = block[32..48];
        const scales = block[48..56];
        const sc = [_]u16{
            std.mem.readInt(u16, scales[0..2], .little),
            std.mem.readInt(u16, scales[2..4], .little),
            std.mem.readInt(u16, scales[4..6], .little),
            std.mem.readInt(u16, scales[6..8], .little),
        };
        const scale_bits = (sc[0] >> 12) | ((sc[1] >> 8) & 0x00F0) | ((sc[2] >> 4) & 0x0F00) | (sc[3] & 0xF000);
        const d = decodeFp16Le(@truncate(scale_bits), @truncate(scale_bits >> 8));

        for (0..8) |ib| {
            const sc_word = sc[ib / 2];
            const sc_shift: u4 = @intCast(6 * (ib % 2));
            const dl = [_]f32{
                d * @as(f32, @floatFromInt(2 * ((sc_word >> sc_shift) & 0x7) + 1)),
                d * @as(f32, @floatFromInt(2 * ((sc_word >> (sc_shift + 3)) & 0x7) + 1)),
            };
            const q_base = ib * 4;
            const h_base = ib * 2;
            const indexes = [_]u16{
                @as(u16, qs[q_base + 0]) | ((@as(u16, qh[h_base + 0]) << 8) & 0x700),
                @as(u16, qs[q_base + 1]) | ((@as(u16, qh[h_base + 0]) << 4) & 0x700),
                @as(u16, qs[q_base + 2]) | ((@as(u16, qh[h_base + 1]) << 8) & 0x700),
                @as(u16, qs[q_base + 3]) | ((@as(u16, qh[h_base + 1]) << 4) & 0x700),
            };
            const deltas = [_]f32{
                if ((qh[h_base + 0] & 0x08) != 0) -0.125 else 0.125,
                if ((qh[h_base + 0] & 0x80) != 0) -0.125 else 0.125,
                if ((qh[h_base + 1] & 0x08) != 0) -0.125 else 0.125,
                if ((qh[h_base + 1] & 0x80) != 0) -0.125 else 0.125,
            };
            for (0..4) |lane| {
                const grid = iq1_s_grid[indexes[lane]];
                const d_lane = dl[lane / 2];
                const delta = deltas[lane];
                for (0..8) |j| {
                    if (out_off >= output.len) return;
                    const trit = (grid >> @as(u4, @intCast(2 * j))) & 0x3;
                    const raw = @as(i32, @intCast(trit)) - 1;
                    output[out_off] = d_lane * (@as(f32, @floatFromInt(raw)) + delta);
                    out_off += 1;
                }
            }
        }
    }
}

pub fn iq2SignMask(sign_index: u7) u8 {
    const low: u8 = sign_index;
    const parity_bit: u8 = @as(u8, @intCast(@popCount(low) & 1)) << 7;
    return low | parity_bit;
}

