        const scale = if (sub_scales[sub] == 0.0) @as(f32, 1.0) else sub_scales[sub];
        const inv_scale = if (sub_scales[sub] == 0.0) 0.0 else 1.0 / scale;

        for (0..16) |i| {
            const q_u = quantizeNibbleSigned(input[sub * 16 + i], inv_scale, 32, 63);
            const l = l_base + i;
            output[ql_off + l] |= (q_u & 0x0F) << nibble_shift;
            output[128 + qh_off + l] |= ((q_u >> 4) & 0x03) << qh_shift;
        }
    }
}

pub fn quantizeQ4_KFromF32(allocator: std.mem.Allocator, input: []const f32) ![]u8 {
    const block_values = tensor_types.valuesPerBlock(.{ .known = .Q4_K }).?;
    const block_bytes = tensor_types.bytesPerBlock(.{ .known = .Q4_K }).?;
    if (input.len == 0 or input.len % block_values != 0) return error.InvalidQuantizedInputSize;

    const block_count = input.len / block_values;
    const output = try allocator.alloc(u8, block_count * block_bytes);
    errdefer allocator.free(output);

    for (0..block_count) |block_idx| {
        quantizeQ4_KBlock(input[block_idx * block_values ..][0..block_values], output[block_idx * block_bytes ..][0..block_bytes]);
    }
    return output;
}

pub fn quantizeQ4_KBlock(input: []const f32, output: []u8) void {
    std.debug.assert(input.len == tensor_types.valuesPerBlock(.{ .known = .Q4_K }).?);
    std.debug.assert(output.len == tensor_types.bytesPerBlock(.{ .known = .Q4_K }).?);

    @memset(output, 0);
    var sub_scales: [8]f32 = undefined;
    var sub_mins: [8]f32 = undefined;
    var scs: [8]u8 = undefined;
    var mins: [8]u8 = undefined;
    var max_scale: f32 = 0.0;
    var max_min: f32 = 0.0;

    for (0..8) |sub| {
        const start = sub * 32;
        const sub_input = input[start .. start + 32];
        var min_v = sub_input[0];
        var max_v = sub_input[0];
        for (sub_input[1..]) |value| {
            min_v = @min(min_v, value);
            max_v = @max(max_v, value);
        }
        const min_mag = @max(-min_v, 0.0);
        const scale = if (max_v + min_mag == 0.0) @as(f32, 1.0) else (max_v + min_mag) / 15.0;
        sub_scales[sub] = scale;
        sub_mins[sub] = min_mag;
        max_scale = @max(max_scale, scale);
        max_min = @max(max_min, min_mag);
    }

    const d = if (max_scale == 0.0) @as(f32, 1.0) else max_scale / 63.0;
    const dmin = if (max_min == 0.0) @as(f32, 1.0) else max_min / 63.0;
    writeF16Le(output[0..2], d);
    writeF16Le(output[2..4], dmin);

    for (0..8) |sub| {
        scs[sub] = if (max_scale == 0.0) 0 else @intFromFloat(@min(@max(std.math.round(sub_scales[sub] / d), 0.0), 63.0));
        mins[sub] = if (max_min == 0.0) 0 else @intFromFloat(@min(@max(std.math.round(sub_mins[sub] / dmin), 0.0), 63.0));
    }
    packQ4KScaleMins(output[4..16], scs, mins);

    for (0..4) |chunk| {
        const sub0 = chunk * 2;
        const sub1 = sub0 + 1;
        const scale0 = if (sub_scales[sub0] == 0.0) @as(f32, 1.0) else sub_scales[sub0];
        const scale1 = if (sub_scales[sub1] == 0.0) @as(f32, 1.0) else sub_scales[sub1];
        const inv0 = if (sub_scales[sub0] == 0.0) 0.0 else 1.0 / scale0;
        const inv1 = if (sub_scales[sub1] == 0.0) 0.0 else 1.0 / scale1;
        const q_off = 16 + chunk * 32;

        for (0..32) |i| {
            const q0 = quantizeUnsigned(input[sub0 * 32 + i] + sub_mins[sub0], inv0, 15);
            const q1 = quantizeUnsigned(input[sub1 * 32 + i] + sub_mins[sub1], inv1, 15);
            output[q_off + i] = q0 | (q1 << 4);
        }
    }
}

fn maxAbs(input: []const f32) f32 {
    var max_abs: f32 = 0.0;
    for (input) |value| {
        max_abs = @max(max_abs, @abs(value));
    }
    return max_abs;
}

fn writeF16Le(dst: []u8, value: f32) void {
    std.debug.assert(dst.len == 2);
    const bits: u16 = @bitCast(@as(f16, @floatCast(value)));
    dst[0] = @truncate(bits);
    dst[1] = @truncate(bits >> 8);
}

fn writeF32Le(dst: []u8, value: f32) void {
    std.debug.assert(dst.len == 4);
    const bits: u32 = @bitCast(value);
    std.mem.writeInt(u32, dst[0..4], bits, .little);
}

fn quantizeNibbleSigned(value: f32, inv_scale: f32, bias: f32, max_q: u8) u8 {
    if (inv_scale == 0.0) return @intFromFloat(bias);
    const quantized = std.math.round(value * inv_scale) + bias;
    return @intFromFloat(@min(@max(quantized, 0.0), @as(f32, @floatFromInt(max_q))));
}

fn quantizeUnsigned(value: f32, inv_scale: f32, max_q: u8) u8 {
    if (inv_scale == 0.0) return 0;
    const quantized = std.math.round(value * inv_scale);
    return @intFromFloat(@min(@max(quantized, 0.0), @as(f32, @floatFromInt(max_q))));
}

fn packQ4KScaleMins(dst: []u8, scs: [8]u8, mins: [8]u8) void {
    std.debug.assert(dst.len == 12);
    dst[0] = (scs[0] & 63) | ((scs[4] >> 4) << 6);
    dst[1] = (scs[1] & 63) | ((scs[5] >> 4) << 6);
    dst[2] = (scs[2] & 63) | ((scs[6] >> 4) << 6);
    dst[3] = (scs[3] & 63) | ((scs[7] >> 4) << 6);
    dst[4] = (mins[0] & 63) | ((mins[4] >> 4) << 6);
    dst[5] = (mins[1] & 63) | ((mins[5] >> 4) << 6);
    dst[6] = (mins[2] & 63) | ((mins[6] >> 4) << 6);
    dst[7] = (mins[3] & 63) | ((mins[7] >> 4) << 6);
    dst[8] = (scs[4] & 0x0F) | ((mins[4] & 0x0F) << 4);
    dst[9] = (scs[5] & 0x0F) | ((mins[5] & 0x0F) << 4);
    dst[10] = (scs[6] & 0x0F) | ((mins[6] & 0x0F) << 4);
    dst[11] = (scs[7] & 0x0F) | ((mins[7] & 0x0F) << 4);
}

fn packQ3KScaleData(dst: []u8, raw_scales: [16]u8) void {
    std.debug.assert(dst.len == 12);
    @memset(dst, 0);
    for (0..4) |i| {
        dst[i] = (raw_scales[i] & 0x0F) | ((raw_scales[i + 8] & 0x0F) << 4);
        dst[4 + i] = (raw_scales[i + 4] & 0x0F) | ((raw_scales[i + 12] & 0x0F) << 4);
        dst[8 + i] = ((raw_scales[i] >> 4) & 0x03) |
            (((raw_scales[i + 4] >> 4) & 0x03) << 2) |
            (((raw_scales[i + 8] >> 4) & 0x03) << 4) |
            (((raw_scales[i + 12] >> 4) & 0x03) << 6);
    }
}

