test "quantize q8_k block matches scalar byte reference" {
    const cases = [_]struct {
        seed: i32,
        scale: f32,
    }{
        .{ .seed = 3, .scale = 0.0 },
        .{ .seed = 5, .scale = 0.03125 },
        .{ .seed = 11, .scale = 0.25 },
        .{ .seed = 17, .scale = 1.75 },
    };

    for (cases) |case| {
        var input: [256]f32 = undefined;
        for (&input, 0..) |*value, idx| {
            const signed: i32 = @intCast((idx * @as(usize, @intCast(case.seed)) + 7) % 67);
            value.* = @as(f32, @floatFromInt(signed - 33)) * case.scale;
            if (idx % 23 == 0) value.* += 0.5 * case.scale;
            if (idx % 29 == 0) value.* -= 0.5 * case.scale;
        }

        var expected: [292]u8 = undefined;
        var actual: [292]u8 = undefined;
        quantizeQ8_KBlockScalarReference(&input, &expected);
        quantizeQ8_KBlock(&input, &actual);
        try std.testing.expectEqualSlices(u8, &expected, &actual);
    }
}

fn quantizeQ8_KBlockScalarReference(input: []const f32, output: []u8) void {
    var max_abs: f32 = 0.0;
    for (input) |value| {
        max_abs = @max(max_abs, @abs(value));
    }
    const scale = if (max_abs == 0.0) @as(f32, 1.0) else max_abs / 127.0;
    writeF32Le(output[0..4], scale);
    if (max_abs == 0.0) {
        @memset(output[4..260], 0);
        @memset(output[260..292], 0);
        return;
    }

    const inv_scale = 1.0 / scale;
    var block_sums: [16]i16 = [_]i16{0} ** 16;
    for (input, 0..) |value, i| {
        const q: i8 = @intFromFloat(@min(@max(@round(value * inv_scale), -127.0), 127.0));
        output[4 + i] = @bitCast(q);
        block_sums[i / 16] += @intCast(q);
    }
    for (block_sums, 0..) |sum, group| {
        std.mem.writeInt(i16, output[260 + group * 2 ..][0..2], sum, .little);
    }
}

