pub const PackedExpertLinearMeta = struct {
    shape: [3]i64,
    expert_axis: usize,
    in_axis: usize,
    out_axis: usize,
    source_out_dim: usize,
    logical_out_dim: usize,
    row_blocks: usize,
    values_per_block: usize,
    block_size: usize,
};

pub fn packedExpertLinearMeta(
    shape: []const i64,
    expert_axis: usize,
    expert_index: u32,
    in_dim: usize,
    out_dim: usize,
    tensor_type: tensor_types.TensorType,
) !PackedExpertLinearMeta {
    if (shape.len != 3) return error.InvalidPackedExpertTensor;
    if (expert_axis >= shape.len) return error.InvalidPackedExpertTensor;
    if (expert_index >= @as(u32, @intCast(shape[expert_axis]))) return error.InvalidPackedExpertTensor;

    const values_per_block = tensor_types.valuesPerBlock(tensor_type) orelse return error.UnsupportedTensorType;
    if (in_dim % values_per_block != 0) return error.InvalidQuantizedLinearShape;
    const block_size = tensor_types.bytesPerBlock(tensor_type) orelse return error.UnsupportedTensorType;

    var axes: [2]usize = undefined;
    var n: usize = 0;
    for (0..shape.len) |axis| {
        if (axis == expert_axis) continue;
        axes[n] = axis;
        n += 1;
    }
    if (n != 2) return error.InvalidPackedExpertTensor;

    for (0..2) |in_slot| {
        const in_axis = axes[in_slot];
        const out_axis = axes[1 - in_slot];
        if (shape[in_axis] != @as(i64, @intCast(in_dim))) continue;
        const source_out_dim_i64 = shape[out_axis];
        const out_dim_i64 = @as(i64, @intCast(out_dim));
        if (source_out_dim_i64 != out_dim_i64 and source_out_dim_i64 != out_dim_i64 * 2) continue;
        if (in_axis != 0 and in_axis != 2) return error.InvalidPackedExpertTensor;
        return .{
            .shape = .{ shape[0], shape[1], shape[2] },
            .expert_axis = expert_axis,
            .in_axis = in_axis,
            .out_axis = out_axis,
            .source_out_dim = @intCast(source_out_dim_i64),
            .logical_out_dim = out_dim,
            .row_blocks = in_dim / values_per_block,
            .values_per_block = values_per_block,
            .block_size = block_size,
        };
    }

    return error.InvalidPackedExpertTensor;
}

fn inferPackedExpertLinearMeta(
    shape: []const i64,
    expert_axis: usize,
    expert_index: u32,
    row_count_override: ?usize,
    tensor_type: tensor_types.TensorType,
) !PackedExpertLinearMeta {
    if (shape.len != 3) return error.InvalidPackedExpertTensor;
    if (expert_axis >= shape.len) return error.InvalidPackedExpertTensor;
    if (expert_index >= @as(u32, @intCast(shape[expert_axis]))) return error.InvalidPackedExpertTensor;

    const values_per_block = tensor_types.valuesPerBlock(tensor_type) orelse return error.UnsupportedTensorType;
    const block_size = tensor_types.bytesPerBlock(tensor_type) orelse return error.UnsupportedTensorType;

    var axes: [2]usize = undefined;
    var n: usize = 0;
    for (0..shape.len) |axis| {
        if (axis == expert_axis) continue;
        axes[n] = axis;
        n += 1;
    }
    if (n != 2) return error.InvalidPackedExpertTensor;

    const preferred_in_axis: usize = if (expert_axis == 2) 0 else 2;
    const candidates = [_]usize{ preferred_in_axis, axes[0], axes[1] };
    for (candidates) |in_axis| {
        if (in_axis == expert_axis) continue;
        if (in_axis != axes[0] and in_axis != axes[1]) continue;
        if (in_axis != 0 and in_axis != 2) continue;
        const in_dim: usize = @intCast(shape[in_axis]);
        if (in_dim == 0 or in_dim % values_per_block != 0) continue;
        const out_axis = if (axes[0] == in_axis) axes[1] else axes[0];
        const source_out_dim: usize = @intCast(shape[out_axis]);
        return .{
            .shape = .{ shape[0], shape[1], shape[2] },
            .expert_axis = expert_axis,
            .in_axis = in_axis,
            .out_axis = out_axis,
            .source_out_dim = source_out_dim,
            .logical_out_dim = row_count_override orelse source_out_dim,
            .row_blocks = in_dim / values_per_block,
            .values_per_block = values_per_block,
            .block_size = block_size,
        };
    }

    return error.InvalidPackedExpertTensor;
}

pub fn packedExpertLinearRowBlockBase(
    meta: PackedExpertLinearMeta,
    expert_index: u32,
    row_index: usize,
    row_offset: usize,
) !usize {
    const source_row = row_index + row_offset;
    if (source_row >= meta.source_out_dim) return error.InvalidPackedExpertTensor;

    var indices = [_]usize{ 0, 0, 0 };
    indices[meta.expert_axis] = expert_index;
    indices[meta.out_axis] = source_row;

    const row_linear = switch (meta.in_axis) {
        0 => indices[1] + @as(usize, @intCast(meta.shape[1])) * indices[2],
        2 => indices[0] * @as(usize, @intCast(meta.shape[1])) + indices[1],
        else => return error.InvalidPackedExpertTensor,
    };
    return std.math.mul(usize, row_linear, meta.row_blocks) catch return error.InvalidPackedExpertTensor;
}

pub fn quantizeQ8_0FromF32(allocator: std.mem.Allocator, input: []const f32) ![]u8 {
    const block_values = tensor_types.valuesPerBlock(.{ .known = .Q8_0 }).?;
    const block_bytes = tensor_types.bytesPerBlock(.{ .known = .Q8_0 }).?;
    if (input.len == 0 or input.len % block_values != 0) return error.InvalidQuantizedInputSize;

    const block_count = input.len / block_values;
    const output = try allocator.alloc(u8, block_count * block_bytes);
    errdefer allocator.free(output);

    for (0..block_count) |block_idx| {
        quantizeQ8_0Block(input[block_idx * block_values ..][0..block_values], output[block_idx * block_bytes ..][0..block_bytes]);
    }
    return output;
}

pub fn quantizeQ8_0Block(input: []const f32, output: []u8) void {
    std.debug.assert(input.len == tensor_types.valuesPerBlock(.{ .known = .Q8_0 }).?);
    std.debug.assert(output.len == tensor_types.bytesPerBlock(.{ .known = .Q8_0 }).?);

    var max_abs: f32 = 0.0;
    for (input) |value| {
        max_abs = @max(max_abs, @abs(value));
    }

    const scale = if (max_abs == 0.0) @as(f32, 1.0) else max_abs / 127.0;
    const half_bits: u16 = @bitCast(@as(f16, @floatCast(scale)));
    output[0] = @truncate(half_bits);
    output[1] = @truncate(half_bits >> 8);

    const inv_scale = if (max_abs == 0.0) 0.0 else 1.0 / scale;
    for (input, 0..) |value, i| {
        const q = if (max_abs == 0.0)
            @as(i8, 0)
        else
            @as(i8, @intFromFloat(@min(@max(std.math.round(value * inv_scale), -127.0), 127.0)));
        output[2 + i] = @bitCast(q);
    }
}

