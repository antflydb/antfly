fn dequantizeQ1_0(data: []const u8, output: []f32) !void {
    const block_size = tensor_types.bytesPerBlock(.{ .known = .Q1_0 }).?;
    const values_per_block = tensor_types.valuesPerBlock(.{ .known = .Q1_0 }).?;
    if (data.len % block_size != 0) return error.InvalidQuantizedDataSize;
    if (output.len > (data.len / block_size) * values_per_block) return error.InvalidQuantizedOutputSize;

    var out_off: usize = 0;
    var block_index: usize = 0;
    while (block_index * block_size < data.len and out_off < output.len) : (block_index += 1) {
        const block = data[block_index * block_size .. (block_index + 1) * block_size];
        const d = decodeFp16Le(block[0], block[1]);
        const qs = block[2..18];
        for (0..128) |i| {
            if (out_off >= output.len) return;
            const bit = (qs[i / 8] >> @as(u3, @intCast(i % 8))) & 1;
            output[out_off] = if (bit != 0) d else -d;
            out_off += 1;
        }
    }
}

fn dequantizeI2S(data: []const u8, output: []f32) !void {
    const block_size = tensor_types.bytesPerBlock(.{ .known = .I2_S }).?;
    const values_per_block = tensor_types.valuesPerBlock(.{ .known = .I2_S }).?;
    const required_blocks = std.math.divCeil(usize, output.len, values_per_block) catch return error.InvalidQuantizedOutputSize;
    const required_bytes = std.math.mul(usize, required_blocks, block_size) catch return error.InvalidQuantizedDataSize;
    if (data.len < required_bytes) return error.InvalidQuantizedDataSize;

    const map2bit = [_]f32{ -1.0, 0.0, 1.0, 0.0 };
    var out_off: usize = 0;
    var block_index: usize = 0;
    while (out_off < output.len) : (block_index += 1) {
        const block_off = block_index * block_size;
        if (block_off + block_size > data.len) return error.InvalidQuantizedDataSize;
        const block = data[block_off .. block_off + block_size];

        for (0..32) |group| {
            const packed_value = block[group];
            const codes = [_]u2{
                @intCast((packed_value >> 6) & 0x03),
                @intCast((packed_value >> 4) & 0x03),
                @intCast((packed_value >> 2) & 0x03),
                @intCast(packed_value & 0x03),
            };
            inline for (0..4) |lane| {
                const dst = out_off + lane * 32 + group;
                if (dst < output.len) output[dst] = map2bit[codes[lane]];
            }
        }
        out_off += values_per_block;
    }
}

fn dequantizeI8S(data: []const u8, output: []f32) !void {
    if (data.len < output.len) return error.InvalidQuantizedDataSize;
    for (output, 0..) |*dst, i| {
        const value: i8 = @bitCast(data[i]);
        dst.* = @floatFromInt(value);
    }
}

