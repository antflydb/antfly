fn dequantizeIQ2XXS(data: []const u8, output: []f32) !void {
    const block_size = tensor_types.bytesPerBlock(.{ .known = .IQ2_XXS }).?;
    const values_per_block = tensor_types.valuesPerBlock(.{ .known = .IQ2_XXS }).?;
    if (data.len % block_size != 0) return error.InvalidQuantizedDataSize;
    if (output.len > (data.len / block_size) * values_per_block) return error.InvalidQuantizedOutputSize;

    var out_off: usize = 0;
    var block_index: usize = 0;
    while (block_index * block_size < data.len and out_off < output.len) : (block_index += 1) {
        const block = data[block_index * block_size .. (block_index + 1) * block_size];
        const d = decodeFp16Le(block[0], block[1]);
        const qs = block[2..66];

        for (0..8) |ib32| {
            const packed_grids = std.mem.readInt(u32, qs[ib32 * 8 ..][0..4], .little);
            const packed_signs_scales = std.mem.readInt(u32, qs[ib32 * 8 + 4 ..][0..4], .little);
            const db = d * (0.5 + @as(f32, @floatFromInt(packed_signs_scales >> 28))) * 0.25;

            for (0..4) |lane| {
                const grid_index: u8 = @truncate(packed_grids >> @as(u5, @intCast(8 * lane)));
                const signs = iq2SignMask(@truncate((packed_signs_scales >> @as(u5, @intCast(7 * lane))) & 0x7F));
                const grid = iq2_xxs_grid[grid_index];
                for (0..8) |j| {
                    if (out_off >= output.len) return;
                    const raw = 2 * @as(i32, @intCast((grid >> @as(u4, @intCast(2 * j))) & 0x03)) + 1;
                    const sign: f32 = if ((signs & (@as(u8, 1) << @as(u3, @intCast(j)))) != 0) -1.0 else 1.0;
                    output[out_off] = db * @as(f32, @floatFromInt(raw)) * sign;
                    out_off += 1;
                }
            }
        }
    }
}

fn dequantizeIQ2XS(data: []const u8, output: []f32) !void {
    const block_size = tensor_types.bytesPerBlock(.{ .known = .IQ2_XS }).?;
    const values_per_block = tensor_types.valuesPerBlock(.{ .known = .IQ2_XS }).?;
    if (data.len % block_size != 0) return error.InvalidQuantizedDataSize;
    if (output.len > (data.len / block_size) * values_per_block) return error.InvalidQuantizedOutputSize;

    var out_off: usize = 0;
    var block_index: usize = 0;
    while (block_index * block_size < data.len and out_off < output.len) : (block_index += 1) {
        const block = data[block_index * block_size .. (block_index + 1) * block_size];
        const d = decodeFp16Le(block[0], block[1]);
        const qs = block[2..66];
        const scales = block[66..74];

        for (0..8) |ib32| {
            const db = [_]f32{
                d * (0.5 + @as(f32, @floatFromInt(scales[ib32] & 0x0F))) * 0.25,
                d * (0.5 + @as(f32, @floatFromInt(scales[ib32] >> 4))) * 0.25,
