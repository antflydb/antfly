def main():
    text = SRC.read_text()
    grids = {
        "iq2_xxs_grid": parse_grid("iq2_xxs_grid", text),
        "iq2_xs_grid": parse_grid("iq2_xs_grid", text),
        "iq2_s_grid": parse_grid("iq2_s_grid", text),
        "iq3_xxs_grid": parse_grid("iq3_xxs_grid", text),
        "iq3_s_grid": parse_grid("iq3_s_grid", text),
        "iq1_s_grid": parse_grid("iq1_s_grid", text),
    }

    SHADERS_DIR.mkdir(parents=True, exist_ok=True)

    for spec in SHADER_SPECS:
        parts = [PROLOG.format(fmt=spec["fmt"], fmt_lower=spec["fmt_lower"])]
        for grid_name, grid_width in spec["grids"]:
            values = grids[grid_name]
            width = 16 if grid_width == "u16" else 32
            parts.append(fmt_array(grid_name, values, width=width))
            if grid_width == "u16":
                parts.append(grid_lookup_u16(grid_name))
            else:
                parts.append(grid_lookup_u32(grid_name))
        parts.append(spec["body"].format(fmt_lower=spec["fmt_lower"]))
        parts.append(EPILOG.format(fmt_lower=spec["fmt_lower"]))
        out_path = SHADERS_DIR / f"matmul_transb_{spec['fmt_lower']}.wgsl"
        out_path.write_text("".join(parts))
        print(f"wrote {out_path.relative_to(ROOT)} ({len(parts)} parts)")


if __name__ == "__main__":
    main()
