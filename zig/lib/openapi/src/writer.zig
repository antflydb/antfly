//! Indented Zig source code writer.
//!
//! Produces formatted Zig source text with proper indentation.

const std = @import("std");
const Allocator = std.mem.Allocator;

pub const SourceWriter = struct {
    buffer: std.ArrayListUnmanaged(u8) = .empty,
    indent_level: u32 = 0,
    allocator: Allocator,
    at_line_start: bool = true,

    pub fn init(allocator: Allocator) SourceWriter {
        return .{ .allocator = allocator };
    }

    pub fn deinit(self: *SourceWriter) void {
        self.buffer.deinit(self.allocator);
    }

    /// Write a formatted line with current indentation, followed by newline.
    pub fn line(self: *SourceWriter, comptime fmt: []const u8, args: anytype) !void {
        try self.writeIndent();
        try self.buffer.print(self.allocator, fmt, args);
        try self.buffer.append(self.allocator, '\n');
        self.at_line_start = true;
    }

    /// Write a blank line.
    pub fn blank(self: *SourceWriter) !void {
        try self.buffer.append(self.allocator, '\n');
        self.at_line_start = true;
    }

    /// Write raw text without indentation or newline.
    pub fn raw(self: *SourceWriter, text: []const u8) !void {
        try self.buffer.appendSlice(self.allocator, text);
        self.at_line_start = text.len > 0 and text[text.len - 1] == '\n';
    }

    /// Increase indentation.
    pub fn indent(self: *SourceWriter) void {
        self.indent_level += 1;
    }

    /// Decrease indentation.
    pub fn dedent(self: *SourceWriter) void {
        if (self.indent_level > 0) {
            self.indent_level -= 1;
        }
    }

    /// Write the standard generated-file header.
    pub fn writeHeader(self: *SourceWriter, package_name: []const u8) !void {
        try self.line("// Code generated by openapi-zig. DO NOT EDIT.", .{});
        try self.line("// Package: {s}", .{package_name});
        try self.blank();
    }

    /// Write a doc comment, splitting multi-line text into separate `///` lines.
    pub fn docComment(self: *SourceWriter, text: []const u8) !void {
        if (text.len == 0) {
            try self.line("///", .{});
            return;
        }
        var iter = std.mem.splitScalar(u8, text, '\n');
        while (iter.next()) |part| {
            const trimmed = std.mem.trimEnd(u8, part, " \t\r");
            if (trimmed.len == 0) {
                try self.line("///", .{});
            } else {
                try self.line("/// {s}", .{trimmed});
            }
        }
    }

    /// Write a regular comment line (not a doc comment).
    pub fn comment(self: *SourceWriter, text: []const u8) !void {
        if (text.len == 0) {
            try self.line("//", .{});
        } else {
            try self.line("// {s}", .{text});
        }
    }

    /// Get the generated source as a slice.
    pub fn toSlice(self: *const SourceWriter) []const u8 {
        return self.buffer.items;
    }

    /// Get the generated source as an owned slice.
    pub fn toOwnedSlice(self: *SourceWriter) ![]u8 {
        return self.buffer.toOwnedSlice(self.allocator);
    }

    fn writeIndent(self: *SourceWriter) !void {
        if (!self.at_line_start) return;
        for (0..self.indent_level) |_| {
            try self.buffer.appendSlice(self.allocator, "    ");
        }
        self.at_line_start = false;
    }
};

test "basic writing" {
    const alloc = std.testing.allocator;
    var w = SourceWriter.init(alloc);
    defer w.deinit();

    try w.line("const std = @import(\"std\");", .{});
    try w.blank();
    try w.line("pub const Foo = struct {{", .{});
    w.indent();
    try w.line("bar: i32,", .{});
    w.dedent();
    try w.line("}};", .{});

    const expected =
        \\const std = @import("std");
        \\
        \\pub const Foo = struct {
        \\    bar: i32,
        \\};
        \\
    ;
    try std.testing.expectEqualStrings(expected, w.toSlice());
}

test "doc comment" {
    const alloc = std.testing.allocator;
    var w = SourceWriter.init(alloc);
    defer w.deinit();

    try w.docComment("A useful type.");
    try w.line("pub const X = i32;", .{});

    const expected =
        \\/// A useful type.
        \\pub const X = i32;
        \\
    ;
    try std.testing.expectEqualStrings(expected, w.toSlice());
}

test "regular comment" {
    const alloc = std.testing.allocator;
    var w = SourceWriter.init(alloc);
    defer w.deinit();

    try w.comment("Handler interface:");
    try w.comment("");
    try w.comment("  fn getStatus(self: *Impl) !Response");
    try w.line("pub const X = i32;", .{});

    const expected =
        \\// Handler interface:
        \\//
        \\//   fn getStatus(self: *Impl) !Response
        \\pub const X = i32;
        \\
    ;
    try std.testing.expectEqualStrings(expected, w.toSlice());
}

test "multi-line doc comment splits on newlines" {
    const alloc = std.testing.allocator;
    var w = SourceWriter.init(alloc);
    defer w.deinit();

    try w.docComment("First line.\nSecond line.\n\nAfter blank.");
    try w.line("pub const X = i32;", .{});

    const expected =
        \\/// First line.
        \\/// Second line.
        \\///
        \\/// After blank.
        \\pub const X = i32;
        \\
    ;
    try std.testing.expectEqualStrings(expected, w.toSlice());
}
